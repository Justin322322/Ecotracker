// Map existing demo headers/types to carbon-tracker semantics
function deriveSectionCategory(header: string, type: string): { section: string; category: string } {
  const text = `${header} ${type}`.toLowerCase()
  if (/(transport|commute|car|travel|bus|bike|bicycle|flight|vehicles?)/.test(text)) {
    if (/(flight|air|plane)/.test(text)) return { section: "Transportation", category: "Flights" }
    if (/(bus|transit|metro|train)/.test(text)) return { section: "Transportation", category: "Public Transit" }
    if (/(bike|bicycle)/.test(text)) return { section: "Transportation", category: "Cycling" }
    return { section: "Transportation", category: "Commute" }
  }
  if (/(energy|kwh|electric|electricity|home)/.test(text)) {
    return { section: "Home Energy", category: "Electricity" }
  }
  if (/(gas|heating|propane|natural gas)/.test(text)) {
    return { section: "Home Energy", category: "Heating" }
  }
  if (/(waste|recycle|landfill|trash)/.test(text)) {
    return { section: "Waste", category: "Household Waste" }
  }
  if (/(diet|food|meal|meat|vegetarian|vegan)/.test(text)) {
    return { section: "Diet", category: "Food" }
  }
  if (/(offset|carbon|tree|credits?)/.test(text)) {
    return { section: "Offsets", category: "Carbon Credits" }
  }
  return { section: "General", category: "Other" }
}

// Create a separate component for the drag handle
function DragHandle({ id }: { id: number }) {
  const { attributes, listeners } = useSortable({
    id,
  })

  return (
    <Button
      {...attributes}
      {...listeners}
      variant="ghost"
      size="icon"
      className="size-7 text-muted-foreground hover:bg-transparent"
    >
      <GripVerticalIcon className="size-3 text-muted-foreground" />
      <span className="sr-only">Drag to reorder</span>
    </Button>
  )
}

